import mongoose from "mongoose";

const CostSchema = new mongoose.Schema(
  {
    code: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
      required: true,
    },
    active: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

const Cost = mongoose.model("Cost", CostSchema);
export default Cost;
